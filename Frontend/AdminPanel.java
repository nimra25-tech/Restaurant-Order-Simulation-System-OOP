    private void btnviewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnviewActionPerformed
       StringBuilder ordersText = new StringBuilder();

    try {
        BufferedReader br = new BufferedReader(new FileReader("orders.txt"));
        String line;

        while ((line = br.readLine()) != null) {
            ordersText.append(line).append("\n");
        }
        br.close();

    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "No orders found!");
        return;
    }
    
    JTextArea area = new JTextArea(20, 40);
    area.setText(ordersText.toString());
    area.setEditable(false);

    JScrollPane pane = new JScrollPane(area);

    JOptionPane.showMessageDialog(this,pane,"ALL CUSTOMER ORDERS",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnviewActionPerformed

