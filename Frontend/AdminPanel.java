    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       String name = txtname.getText();
       String Category = txtcategory.getText();
       double price;
    try {
       price = Double.parseDouble(txtprice.getText());
       } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Enter a valid price!");
       return;
       }
        MenuItem item = new MenuItem(0,name,price,Category);
        menuManager.addMenuToDB(item);
        menuManager.loadMenuFromDB();
        loadMenu(); 
       JOptionPane.showMessageDialog(this,"Item added!");
       txtid.setText("");
       txtname.setText("");
       txtprice.setText("");
       txtcategory.setText(""); 
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
       int id;
       double price;
       String category;
        
        try {
           id = Integer.parseInt(txtid.getText());
       } catch (NumberFormatException e) {
       JOptionPane.showMessageDialog(this, "Enter a valid id!");
       return;
      }
      String name = txtname.getText();
     
      try {
        price = Double.parseDouble(txtprice.getText());
       } catch (NumberFormatException e) {
       JOptionPane.showMessageDialog(this, "Enter a valid price!");
       return;
      }
      
      if (txtname.getText().isEmpty() || txtprice.getText().isEmpty()) {
    JOptionPane.showMessageDialog(this, "All fields are required!");
    return;
     }
     category = txtcategory.getText();
    menuManager.updateMenuInDB(id, name, price, category);
    menuManager.loadMenuFromDB();
    loadMenu();
    JOptionPane.showMessageDialog(this, "Item updated!");
       txtid.setText("");
       txtname.setText("");
       txtprice.setText("");
       txtcategory.setText(""); 
    }//GEN-LAST:event_btnEditActionPerformed

